// Problem 02 : you have a basket of apples but you decide to give some away to your neighbor. you want to know how many apples you have left after giving some away.

// Dis : create a function that subtracts two numbers and returns the result.
function CalculateLeftApples(totalApples, givenApples) {
  return totalApples - givenApples;
}
console.log(CalculateLeftApples(15, 2));
